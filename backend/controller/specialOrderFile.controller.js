const multer = require("multer");
const path = require("path");
const fs = require("fs");

// Set up multer storage configuration (define the destination and filename)
const upload = multer({
  dest: "uploads/", // This is where files are temporarily stored before being processed
  filename: (req, file, cb) => {
    const fileName = `bouquet_${Date.now()}.glb`;
    cb(null, fileName); // Set the name of the uploaded file
  },
});

const saveFile = async (req, res) => {
  try {
    // Handle file upload
    upload.single("file")(req, res, (err) => {
      if (err) {
        console.error("Error uploading file:", err);
        return res.status(500).send("Error uploading file");
      }

      // If upload is successful, move the file to the desired location
      const fileName = req.file.filename; // Get the file name generated by multer
      const destinationPath = path.join(
        __dirname,
        "..",
        "assets",
        "specialOrders",
        fileName
      );

      // Move the file from the temporary upload folder to the special order folder
      fs.rename(
        path.join(__dirname, "uploads", fileName),
        destinationPath,
        (err) => {
          if (err) {
            console.error("Error moving file:", err);
            return res.status(500).send("Error saving file");
          }

          // Send back the file name as response
          res.status(200).json({ fileName });
        }
      );
    });
  } catch (error) {
    console.error(error);
    res.status(500).send("Error saving file");
  }
};

module.exports = {
  saveFile,
};
